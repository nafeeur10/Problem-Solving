<?php
 
$x = 10;
$y = 5;

echo "----------------------------------------------\n";
echo "Before Swapping: x = ", $x,", ", "y = ", $y;
echo "\n----------------------------------------------\n\n";

/*
    10 = 1010
    5  = 0101
 -----------------
   xor = 1111
*/
// x now becomes 15 (1111)
$x = $x ^ $y;

/*
    15 = 1111
    5  = 0101
 -----------------
   xor = 1010
*/
 
// y becomes 10 (1010)
$y = $x ^ $y;
 
/*
    15 = 1111
    10 = 1010
 -----------------
   xor = 1010
*/
// x becomes 5 (0101)
$x = $x ^ $y;

echo "----------------------------------------------\n";
echo "After Swapping: x = ", $x,", ", "y = ", $y;
echo "\n----------------------------------------------\n\n";
 
?>
